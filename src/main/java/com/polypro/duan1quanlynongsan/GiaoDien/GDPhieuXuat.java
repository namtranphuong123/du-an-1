    private void btnTimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKiemActionPerformed
        // TODO add your handling code here:
        if (txtTimKiem.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Bạn Chưa Nhập Dữ Liệu Tìm Kiếm");
            return;
        }
        PhieuXuatDao Dao = new PhieuXuatDao();
        try {
            List<PhieuXuat> list = Dao.selectByKeyword(txtTimKiem.getText());
            if (list != null) {
                model.setRowCount(0);
                for (PhieuXuat nh : list) {
                    Object[] column = {nh.getMaPX(), nh.getNgayxuat(), nh.getMaKH(), nh.getDonGia(), nh.getMaNS(), nh.getMaNV(), nh.getSoLuong()};
                    model.addRow(column);
                }
                model.fireTableDataChanged();
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
        }

    }//GEN-LAST:event_btnTimKiemActionPerformed

