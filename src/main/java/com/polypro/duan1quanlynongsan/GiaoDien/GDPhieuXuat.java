    private void btnInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertActionPerformed
        StringBuilder sb = new StringBuilder();
        if (txtMaPX.getText().equals("")) {
            sb.append("Mã phiếu xuất không được để trống \n");
        }

        if (txtDonGia.getText().equals("")) {
            sb.append("Đơn giá  không được để trống \n");
        }
        if (txtNgayXuat.getText().equals("")) {
            sb.append("Ngày xuất không được để trống \n");
        }
        if (txtMaNV.getText().equals("")) {
            sb.append("Mã nhân viên không được để trống \n");
        }

        if (txtSoLuong.getText().equals("")) {
            sb.append("Số lượng không được để trống \n");
        }

        if (sb.length() > 0) {
            JdialogHelper.errordialog(this, sb.toString(), "Lỗi Nhập Dữ Liệu");
            return;
        }
        StringBuilder sa = new StringBuilder();

        String kiemtramapx = "PX\\d{5}";
        if (!txtMaPX.getText().matches(kiemtramapx)) {
            sa.append("Mã phiếu xuất bắt đầu bằng PX và theo sao 5 Ký tự số \n");
        }

        if (sa.length() > 0) {
            JdialogHelper.errordialog(this, sa.toString(), "Lỗi Nhập Dữ Liệu");
            return;
        }

        insert();
        fillTable();

    }//GEN-LAST:event_btnInsertActionPerformed

