    public void initcbo() {
        NongSanDao dao = new NongSanDao();
        List<NongSan> cd = dao.selectAll();
        for (NongSan list : cd) {
            cbo.addElement(list.getTenNS());
        }
        cboncc.setModel(cbo);
        
    }
public void kh(){
    KhachHangDao dkh = new KhachHangDao();
        List<KhachHang> kh = dkh.selectAll();
        for (KhachHang list : kh) {
            aaa.addElement(list.getHoTen());

        }
        cboKh.setModel(aaa);
}
