    private void btnTimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKiemActionPerformed
        if (txttimkiem.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Bạn Chưa Nhập Dữ Liệu Tìm Kiếm");
            return;
        }
        KhachHangDao Dao = new KhachHangDao();
        try {
            List<KhachHang> list = Dao.selectByKeyword(txttimkiem.getText());
            if (list != null) {
                model.setRowCount(0);
                for (KhachHang nh : list) {
                    Object[] column = {nh.getMaKH(), nh.getHoTen(), nh.getNgaySinh(), nh.getGioiTinh(), nh.getDiaChi(),
                        nh.getEmail(), nh.getDienThoai(), nh.getMaNV()};
                    model.addRow(column);
                }
                model.fireTableDataChanged();
              
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnTimKiemActionPerformed

