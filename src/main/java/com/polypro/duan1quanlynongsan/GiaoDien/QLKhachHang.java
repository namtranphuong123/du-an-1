    private void btnInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertActionPerformed
        StringBuilder sb = new StringBuilder();
        if (txtmakh.getText().equals("")) {
            sb.append("Mã khách hàng không được để trống \n");
        }
        if (txthoten.getText().equals("")) {
            sb.append("Họ Tên khách hàng không được để trống \n");
        }
        if (txtNgaySinh.getText().equals("")) {
            sb.append("Ngày Sinh khách hàng không được để trống \n");
        }
        if (txtdiachi.getText().equals("")) {
            sb.append("Địa Chỉ khách hàng không được để trống \n");
        }
        if (txtemail.getText().equals("")) {
            sb.append("Email khách hàng không được để trống \n");
        }
        if (txtdienthoai.getText().equals("")) {
            sb.append("Số điện thoại khách hàng không được để trống \n");
        }
        if (jTextField1.getText().equals("")) {
            sb.append("Người Tạo khách hàng không được để trống \n");
        }

        if (sb.length() > 0) {
            JdialogHelper.errordialog(this, sb.toString(), "Lỗi Nhập Dữ Liệu");
            return;
        }
        StringBuilder sa = new StringBuilder();
        String kiemtrasodt = "0\\d{9}";
        if (!txtdienthoai.getText().matches(kiemtrasodt)) {
            sa.append("Số điện thoại phải bắt đầu từ 0 và có 10 số \n");
        }
        String kiemtramanh = "KH\\d{5}";
        if (!txtmakh.getText().matches(kiemtramanh)) {
            sa.append("Mã Khách Hàng Bắt đầu bằng KH và theo sao 5 Ký tự số \n");
        }
        String gmail = "\\w+\\@+gmail+\\.+com";
        if (!txtemail.getText().matches(gmail)) {
            sa.append("gmail có định dạng @gmail.com \n");
        }
        if (sa.length() > 0) {
            JdialogHelper.errordialog(this, sa.toString(), "Lỗi Nhập Dữ Liệu");
            return;
        }
        insert();
        fillTable();
    }//GEN-LAST:event_btnInsertActionPerformed

