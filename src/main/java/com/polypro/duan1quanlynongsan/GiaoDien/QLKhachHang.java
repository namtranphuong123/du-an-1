    public void ininitTable() {
        model.setColumnIdentifiers(new String[]{"Mã Khách Hàng", "Họ Tên", "Ngày Sinh", "Giới Tính", "địa chỉ", "Email", "Điện Thoại", "Người Tạo"});
        tblKhachHang.setModel(model);
    }

    public void fillTable() {
        KhachHangDao Dao = new KhachHangDao();
        model.setRowCount(0);
        List<KhachHang> list = Dao.selectAll();
        for (KhachHang nh : list) {
            Object[] column = {nh.getMaKH(), nh.getHoTen(), nh.getNgaySinh(), nh.getGioiTinh(), nh.getDiaChi(), nh.getEmail(), nh.getDienThoai(),
                nh.getMaNV()};
            model.addRow(column);
        }
        model.fireTableDataChanged();
    }

    void edit() {
        try {
            String makh = (String) tblKhachHang.getValueAt(this.index, 0);
            KhachHangDao dao = new KhachHangDao();
            KhachHang nv = dao.selectById(makh);
            String nam = null;
            if (nv != null) {
                if (nv.getGioiTinh() == 0) {
                    nam = "Nam";
                } else {
                    nam = "Nữ";
                }
                txtmakh.setText(nv.getMaKH());//
                txthoten.setText(nv.getHoTen());//
                txtNgaySinh.setText(DateHelper.toDateString(nv.getNgaySinh()));
                jComboBox1.setSelectedItem(nam);//
                txtdiachi.setText(nv.getDiaChi());//
                txtemail.setText(nv.getEmail());//
                txtdienthoai.setText(nv.getDienThoai());//
                jTextField1.setText(nv.getMaNV());

                maNV = nv.getMaNV();

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi");
        }
    }

    boolean check() {
        if (txthoten.getText().length() == 0 || txtdienthoai.getText().length() == 0 || txtemail.getText().length() == 0 || txtdiachi.getText().length() == 0) {
            JOptionPane.showMessageDialog(this, "Hãy điền đủ thông tin");
            return false;
        }
        return true;
    }

