    private void btnInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertActionPerformed
        StringBuilder sb = new StringBuilder();
        if (txtMaNV.getText().equals("")) {
            sb.append("Mã Nhân Viên Không Được Để Trống \n");
        }
        if (txtMatKhau.getPassword().equals("")) {
            sb.append("Mật Khẩu Không Được Để Trống \n");
        }
        if (txtXacNhanMK.getPassword().equals("")) {
            sb.append("Xác nhận Mật Khẩu Không Được Để Trống \n");
        }
        if (txtHoTen.getText().equals("")) {
            sb.append("Họ Tên Không Được Để Trống \n");
        }if (txtCMND.getText().equals("")) {
               sb.append("Số Chứng Minh Không Được Để Trống  \n");
        }
       
        if (txtCMND.getText().length()!=9) {
            sb.append("Số Chứng Minh  phải đủ 9 kí tự \n");
        }
        String gmail="\\w+\\@+gmail+\\.+com";
        if(!txtEmail.getText().matches(gmail)){
            sb.append("gmail có định dạng @gmail.com \n");
        }
        if (txtGhiChu.getText().equals("")) {
            sb.append("Ghi Chú Không Được Để Trống \n");
        }
        if (sb.length() > 0) {
            JdialogHelper.errordialog(this, sb.toString(), "Lỗi text");
            return;
        }

        String mk = new String(txtMatKhau.getPassword());
        String mkk = new String(txtXacNhanMK.getPassword());
        if (!mk.equals(mkk)) {
            JdialogHelper.errordialog(this, "Xác Nhận Mật Khẩu Không Khớp", "lỗi mk");
            return;
        }
        NhanVien nv = new NhanVien();
        nv.setMaNV(txtMaNV.getText());
        nv.setHoTen(txtHoTen.getText());
        nv.setMatKhau(new String(txtMatKhau.getPassword()));
        nv.setSoCMND(txtCMND.getText());
        nv.setEmail(txtEmail.getText());
        nv.setGhiChu(txtGhiChu.getText());
        nv.setVaiTro(rdoNhanVien.isSelected() ? 1 : 0);
       nv.setVaiTro(rdoChuKho.isSelected() ? 0 : 1);
        NhanVienDAO dao = new NhanVienDAO();
        dao.insert(nv);
        JdialogHelper.showmessager(this, "Thêm Vào Thành Công", "Thông Báo");
        loadTable();
        clear();
    }//GEN-LAST:event_btnInsertActionPerformed

