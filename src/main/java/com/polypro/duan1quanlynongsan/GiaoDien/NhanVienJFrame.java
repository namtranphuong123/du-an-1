    public void clear() {
        txtMaNV.setText("");
        txtMatKhau.setText("");
        txtXacNhanMK.setText("");
        txtHoTen.setText("");
        txtEmail.setText("");
         txtCMND.setText("");
        txtGhiChu.setText("");
    }

    void edit() {
        try {
            String manv = (String) tblGirdView.getValueAt(this.index, 0);
            NhanVienDAO dao = new NhanVienDAO();
            NhanVien nv = dao.selectById(manv);
            if (nv != null) {
                txtMaNV.setText(nv.getMaNV());
                txtMatKhau.setText(nv.getMatKhau());
                txtXacNhanMK.setText(nv.getMatKhau());
                txtHoTen.setText(nv.getHoTen());
                txtCMND.setText(nv.getSoCMND());   
                txtEmail.setText(nv.getEmail());
                txtGhiChu.setText(nv.getGhiChu());
                rdoNhanVien.setSelected(nv.getVaiTro() == 1 ? true : false);
                rdoChuKho.setSelected(nv.getVaiTro() == 0 ? true : false);
              
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi");
        }
    }

    public void initTable() {
        model.setColumnIdentifiers(new String[]{"Mã Nhân Viên", "Họ Tên","Email","CMND ","Ghi Chú","Vai Trò"});
        tblGirdView.setModel(model);
    }

    public void loadTable() {
        NhanVienDAO Dao = new NhanVienDAO();
        model.setRowCount(0);
        String vaitro = null;
        try {
            List<NhanVien> list = Dao.selectAll();
            for (NhanVien nv : list) {
                if (nv.getVaiTro() == 0) {
                    vaitro = "Nhân Viên";
                }
                if (nv.getVaiTro() == 1) {
                    vaitro = "Chủ";
                }
                
                Object[] row = {nv.getMaNV(), nv.getHoTen(),nv.getEmail(),nv.getSoCMND(),nv.getGhiChu(), vaitro
                };
                model.addRow(row);
            }
            model.fireTableDataChanged();
        } catch (Exception e) {
        }

    }

