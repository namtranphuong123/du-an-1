    public void fillTable() {
       NongSanDao Dao = new NongSanDao();
        model.setRowCount(0);
        List<NongSan> list = Dao.selectAll();
        for (NongSan nh : list) {
            Object[] column = {nh.getMaNS(), nh.getTenNS(), nh.getDVT(), nh.getNgaySX(), nh.getNgayHH(),nh.getGia(),nh.getNCC(),nh.getMaNV()};
            model.addRow(column);
        }
        model.fireTableDataChanged();
    }

