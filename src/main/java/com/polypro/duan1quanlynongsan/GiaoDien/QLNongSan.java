    private void btnTimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKiemActionPerformed
        if (txttimkiem.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Bạn Chưa Nhập Dữ Liệu Tìm Kiếm");
            return;
        }
        NongSanDao Dao = new NongSanDao();
        try {
            List<NongSan> list = Dao.selectByKeyword(txttimkiem.getText());
            if (list != null) {
                model.setRowCount(0);
                for (NongSan nh : list) {
                    Object[] column = {nh.getMaNS(), nh.getTenNS(), nh.getDVT(), nh.getNgaySX(), nh.getNgayHH()};
                    model.addRow(column);
                }
                model.fireTableDataChanged();
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnTimKiemActionPerformed

