    private void btnInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertActionPerformed
        StringBuilder sb = new StringBuilder();
        if (txtmasp.getText().equals("")) {
            sb.append("Mã Nông Sản không được để trống \n");
        }
        if (txthoten.getText().equals("")) {
            sb.append("tên Nông Sản được để trống \n");
        } if (txtdonvitinh.getText().equals("")) {
            sb.append("DVT không được để trống \n");
        }
        if (txtngaysx.getText().equals("")) {
            sb.append("Ngày Sản Xuất không được để trống \n");
        }
        if (txtngayhh.getText().equals("")) {
            sb.append("Ngày Hết Hạn không được để trống \n");
        }
        if (sb.length() > 0) {
            JdialogHelper.errordialog(this, sb.toString(), "Lỗi Nhập Dữ Liệu");
            return;
        }
        if (sb.length() > 0) {
            JdialogHelper.errordialog(this, sb.toString(), "Lỗi Nhập Dữ Liệu");
            return;
        }
        insert();
        fillTable();
        clear();
    }//GEN-LAST:event_btnInsertActionPerformed

